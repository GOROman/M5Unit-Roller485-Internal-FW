void hard_uart_begin(void)
{
  LL_DMA_SetPeriphAddress(DMA1, LL_DMA_CHANNEL_2, LL_USART_DMA_GetRegAddr(USART3, LL_USART_DMA_REG_DATA_RECEIVE));
  LL_DMA_SetMemoryAddress(DMA1, LL_DMA_CHANNEL_2, (uint32_t)dat_Uart1.pRxBuf);
  LL_DMA_SetDataLength(DMA1, LL_DMA_CHANNEL_2, UART1_RX_BUF_SIZE);
    
  LL_USART_EnableIT_IDLE(USART3);
    
  LL_DMA_EnableIT_TC(DMA1, LL_DMA_CHANNEL_2);
  LL_DMA_EnableIT_TE(DMA1, LL_DMA_CHANNEL_2);
    
  LL_DMA_EnableChannel(DMA1, LL_DMA_CHANNEL_2);
  LL_USART_EnableDMAReq_RX(USART3);
}

void UART_DMA_Send(USART_TypeDef *USARTx,uint8_t *str,uint16_t size)
{
  LL_DMA_SetPeriphAddress(DMA1, LL_DMA_CHANNEL_4, LL_USART_DMA_GetRegAddr(USART3, LL_USART_DMA_REG_DATA_TRANSMIT));
	//配置内存地址
  LL_DMA_SetMemoryAddress(DMA1,LL_DMA_CHANNEL_4,(uint32_t)dat_Uart1.pTxBuf);
  //配置数据长度
  LL_DMA_SetDataLength(DMA1,LL_DMA_CHANNEL_4,size);  
  //清除TC标志
  LL_USART_ClearFlag_TC(USARTx); 
  //使能TCIE中断
  LL_USART_EnableIT_TC(USARTx);
  LL_DMA_EnableChannel(DMA1, LL_DMA_CHANNEL_4);
  LL_USART_EnableDMAReq_TX(USART3);
}
